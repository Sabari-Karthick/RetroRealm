package com.Batman.exception.wrapper;


public class VerificationMissingException extends RuntimeException {
	
	private static final long serialVersionUID = 1L;
	
	public VerificationMissingException() {
		super();
	}
	
	public VerificationMissingException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public VerificationMissingException(String message) {
		super(message);
	}
	
	public VerificationMissingException(Throwable cause) {
		super(cause);
	}
	
	
	
}










