package com.Batman.exception.wrapper;


public class RecordNotAvailableException extends RuntimeException {
	
	private static final long serialVersionUID = 1L;
	
	public RecordNotAvailableException() {
		super();
	}
	
	public RecordNotAvailableException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public RecordNotAvailableException(String message) {
		super(message);
	}
	
	public RecordNotAvailableException(Throwable cause) {
		super(cause);
	}
	
	
	
}










